// Calculate a vector normal to the ABC triangle
float3 get_normal(float3 A, float3 B, float3 C) {
    const float3 E_1 = make_float3(B.x - A.x, B.y - A.y, B.z - A.z);
    const float3 E_2 = make_float3(C.x - A.x, C.y - A.y, C.z - A.z);
    const float3 cross = make_float3(E_1.y * E_2.z - E_1.z * E_2.y, E_1.z * E_2.x - E_1.x * E_2.z, E_1.x * E_2.y - E_1.y * E_2.x);
    const float len = std::sqrt(cross.x * cross.x + cross.y * cross.y + cross.z * cross.z);

    return((len > 0.00) ? make_float3(cross.x / len, cross.y / len, cross.z / len) : make_float3(0.00, 0.00, 0.00));
}


// Save a mesh to a binary STL file
void save_binary_STL(const std::string& filename, const std::vector<std::array<float3, 3>>& triangles) 
{
    const char header[80] = { 0 };
    const char zeros[2] = { 0 };
    std::ofstream file;
    uint32_t num_el;

    file.open(filename, std::ofstream::out | std::ofstream::binary | std::ofstream::trunc);
    if (!file) {
        std::cerr << "Cannot create file " + filename << std::endl;
        throw std::runtime_error("Cannot create file " + filename);
    }
    num_el = static_cast<uint32_t>(triangles.size());
    file.write(header, 80 * sizeof(*header));
    file.write(reinterpret_cast<char*>(const_cast<uint32_t*>(&num_el)), sizeof(num_el));
    for (const std::array<float3, 3> &tri : triangles) {
        const float3 normal = get_normal(tri[0], tri[1], tri[2]);
        file.write(reinterpret_cast<char*>(const_cast<float3*>(&normal)), sizeof(normal));
        file.write(reinterpret_cast<char*>(const_cast<std::array<float3, 3> *>(&tri)), sizeof(tri));
        file.write(zeros, 2 * sizeof(*zeros));
        if (!file.good()) {
            std::cerr << "Cannot write to file " + filename << std::endl;
            throw std::runtime_error("Cannot write to file " + filename);
        }
    }
    file.close();
    return;
}

__device__ __forceinline__ float3 vertex_interp(const float3& P_1, const float3& P_2, float alpha_1, float alpha_2, float threshold)
{
    const float ell = __fdividef((alpha_1 - threshold), (alpha_2 - alpha_1 + 1e-10));
    const float x = P_1.x - ell * (P_2.x - P_1.x);
    const float y = P_1.y - ell * (P_2.y - P_1.y);
    const float z = P_1.z - ell * (P_2.z - P_1.z);

    return(make_float3(x, y, z));
}

__global__ void marching_cubes_kernel(
    std::array<float3, 3>* mesh_dev, 
    float3* mid_points_dev,
    float3* mid_normals_dev,
    unsigned long long int* num_triangles_dev, unsigned long long int mesh_buffer_size, 
    float* voxel_values_dev, float3* voxel_poses_dev,
    uint3 num_voxels,
    float threshold, int* edge_table_dev, my_array<char, 16>* triangle_table_dev)
{
    const unsigned int n_x = blockIdx.x;
    const unsigned int n_y = blockIdx.y;
    const unsigned int n_z = blockIdx.z;
    __shared__ float3 vertex_list[12];
    __shared__ grid_cell_t grid;
    __shared__ int cube_index;

    if (threadIdx.x < 8)
    {
        const bool bit_0 = (threadIdx.x & 0x01) != 0;
        const bool bit_1 = (threadIdx.x & 0x02) != 0;
        const bool bit_2 = (threadIdx.x & 0x04) != 0;
        const int m_x = n_x + ((bit_0 != bit_1) ? 1 : 0);
        const int m_y = n_y + (bit_1 ? 1 : 0);
        const int m_z = n_z + (bit_2 ? 0 : 1);
     /*   grid.pos[threadIdx.x] = make_float3(static_cast<float>(m_x) + 0.50f, static_cast<float>(m_y) + 0.50f, static_cast<float>(m_z) + 0.50f);

        grid.pos[threadIdx.x].x = grid.pos[threadIdx.x].x * scale.x + offset.x;
        grid.pos[threadIdx.x].y = grid.pos[threadIdx.x].y * scale.y + offset.y;
        grid.pos[threadIdx.x].z = grid.pos[threadIdx.x].z * scale.z + offset.z;*/

        grid.pos[threadIdx.x] = voxel_poses_dev[map(num_voxels, make_uint3(m_x, m_y, m_z))];
        grid.values[threadIdx.x] = voxel_values_dev[map(num_voxels, make_uint3(m_x, m_y, m_z))];
    }
    __syncthreads();

    if (threadIdx.x == 0)
    {
        cube_index = 0;
        for (int i = 8; i > 0; --i)
        {
            cube_index <<= 1;
            cube_index += (grid.values[i - 1] > threshold);
        }
    }
    __syncthreads();

    if (edge_table_dev[cube_index] != 0)
    {
        if (edge_table_dev[cube_index] & (1 << threadIdx.x))
        {
            const int index_1 = threadIdx.x & 0x07;
            const int index_2 = (threadIdx.x < 4) ? ((threadIdx.x + 1) & 0x03) : ((threadIdx.x < 8) ? (4 + ((threadIdx.x - 3) & 0x03)) : (threadIdx.x - 4));
            vertex_list[threadIdx.x] = vertex_interp(grid.pos[index_1], grid.pos[index_2], grid.values[index_1], grid.values[index_2], threshold);
        }
        __syncthreads();
        if (threadIdx.x == 0)
        {
            unsigned long long int num_to_add = 0;
            for (std::size_t i = 0; triangle_table_dev[cube_index][i] != -1; i += 3)
            {
                ++num_to_add;
            }
            if (num_to_add > 0)
            {
                unsigned long long int index = atomicAdd(num_triangles_dev, num_to_add);
                for (std::size_t i = 0; triangle_table_dev[cube_index][i] != -1; i += 3) {
                    const float3 P_0 = vertex_list[triangle_table_dev[cube_index][i + 0]];
                    const float3 P_1 = vertex_list[triangle_table_dev[cube_index][i + 1]];
                    const float3 P_2 = vertex_list[triangle_table_dev[cube_index][i + 2]];
                    mesh_dev[index] = { P_0, P_1, P_2 };

                    mid_points_dev[index] = make_float3((P_0.x + P_1.x + P_2.x) / 3.0f,
                                                        (P_0.y + P_1.y + P_2.y) / 3.0f,
                                                        (P_0.z + P_1.z + P_2.z) / 3.0f);

                    float3 vec_u = make_float3(P_1.x - P_0.x, P_1.y - P_0.y, P_1.z - P_0.z);
                    float3 vec_v = make_float3(P_2.x - P_0.x, P_2.y - P_0.y, P_2.z - P_0.z);

                    float3 vec_cross = make_float3(vec_u.y * vec_v.z - vec_u.z * vec_v.y,
                                                   vec_u.z * vec_v.x - vec_u.x * vec_v.z,
                                                   vec_u.x * vec_v.y - vec_u.y * vec_v.x);

                    
                    float normal_scale = -__frcp_rn(__fsqrt_rn(vec_cross.x * vec_cross.x +
                                                              vec_cross.y * vec_cross.y +
                                                              vec_cross.z * vec_cross.z) + 1e-8);

                    mid_normals_dev[index] = make_float3(vec_cross.x * normal_scale, vec_cross.y * normal_scale, vec_cross.z * normal_scale);

                    ++index;
                }
            }
        }
    }
    return;
}

