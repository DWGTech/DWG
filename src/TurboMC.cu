int TurboMC::TurboMarchingCubes(float* voxel_values_dev, float3* voxel_poses_dev, int32_t res_x, int32_t res_y, int32_t res_z, float threshold, int iterations, std::string mesh_path, std::string mesh_full_path, float3* mid_points_o_dev, float3* mid_normals_o_dev, int test_iter, cudaStream_t stream)
{
    unsigned long long int num_triangles;
    std::vector<std::array<float3, 3>> mesh;

    int* edge_table_dev = 0;
    my_array<char, 16>* triangle_table_dev = 0;
    cudaMallocManaged((char**)&edge_table_dev, edge_table.size() * sizeof(*edge_table_dev));
    cudaMallocManaged((char**)&triangle_table_dev, triangle_table.size() * sizeof(*triangle_table_dev));
    cudaMemcpy(edge_table_dev, edge_table.data(), edge_table.size() * sizeof(edge_table[0]), cudaMemcpyHostToDevice);
    cudaMemcpy(triangle_table_dev, triangle_table.data(), triangle_table.size() * sizeof(triangle_table[0]), cudaMemcpyHostToDevice);

    uint3 num_voxels = make_uint3(res_x, res_y, res_z);
    std::array<float3, 3>* mesh_dev;
    const unsigned long long int mesh_buffer_size = std::max(num_voxels.x * num_voxels.y * num_voxels.z / 2, 1000000u);	// Maximum number of triangles in the mesh
    cudaMallocManaged((char**)&mesh_dev, mesh_buffer_size * sizeof(*mesh_dev));

    unsigned long long int* num_triangles_dev = 0;
    cudaMallocManaged((char**)&num_triangles_dev, sizeof(*num_triangles_dev));
    num_triangles = 0;
    cudaMemcpy(num_triangles_dev, &num_triangles, sizeof(*num_triangles_dev), cudaMemcpyHostToDevice);

    const dim3 num_blocks = make_uint3(num_voxels.x - 1, num_voxels.y - 1, num_voxels.z - 1);
    const unsigned int block_size = 12;

    marching_cubes_kernel << <num_blocks, block_size, 0, stream >> > (mesh_dev, mid_points_o_dev, mid_normals_o_dev, num_triangles_dev, mesh_buffer_size, voxel_values_dev, voxel_poses_dev, num_voxels, threshold, edge_table_dev, triangle_table_dev);

    cudaMemcpy(&num_triangles, num_triangles_dev, sizeof(*num_triangles_dev), cudaMemcpyDeviceToHost);

    std::cout << "Num triangles: " << num_triangles << std::endl;

    if (((iterations % test_iter == 0) && (test_iter != 999999)) || iterations == 123456)
    {
        mesh.resize(num_triangles);
        cudaMemcpy(mesh.data(), mesh_dev, num_triangles * sizeof(mesh_dev[0]), cudaMemcpyDeviceToHost);
        
        auto now = std::chrono::system_clock::now();
        auto time_t_now = std::chrono::system_clock::to_time_t(now);
        auto local_time = *std::localtime(&time_t_now);
        
        std::string mesh_filename;
        
        if (iterations != 123456)
        {
            mesh_filename = mesh_path + std::to_string(local_time.tm_mon + 1) + std::to_string(local_time.tm_mday) +
                std::to_string(local_time.tm_hour) + std::to_string(local_time.tm_min) + std::to_string(local_time.tm_sec) + "_" + std::to_string(iterations) +
                "iter" + ".stl";
        }
        else
        {
            mesh_filename = mesh_full_path + ".stl";
        }

        
        save_binary_STL(mesh_filename, mesh);
        std::cout << "Mesh saved to " << mesh_filename << " (" << mesh.size() << " triangle(s)." << std::endl;
    }

    cudaFree(edge_table_dev);
    cudaFree(triangle_table_dev);
    //cudaFree(mid_points_dev);
    //cudaFree(mid_normals_dev);
    cudaFree(mesh_dev);
    cudaFree(num_triangles_dev);

    return num_triangles;
}
